"""Tests for the immutable publishing pipeline (auteur publish)."""

from __future__ import annotations

import re
import zipfile
from pathlib import Path

import pytest
import yaml

from auteur.expression.book import BookExpressionStore
from auteur.publish import (
    ALL_FORMATS,
    PublishError,
    PublishingSnapshot,
    publish,
)


def _make_book(tmp_path: Path) -> Path:
    project = tmp_path / "project"
    project.mkdir(parents=True, exist_ok=True)
    from conftest import copy_bootstrap_template as _cbt
    _cbt(project)
    book = BookExpressionStore(project).compose(
        ["chapter_01", "chapter_02"], title="The Lantern at Low Water"
    )
    BookExpressionStore(project).accept(book["book_expression_id"])
    return project


# ---------------------------------------------------------------------------
# PublishingSnapshot unit tests
# ---------------------------------------------------------------------------


class TestPublishingSnapshot:
    def test_reads_accepted_book(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        snapshot = PublishingSnapshot(project)
        assert snapshot.title == "The Lantern at Low Water"
        assert snapshot.author == "Unknown Author"
        assert snapshot._manifest["revision"] >= 1
        assert len(snapshot._parsed["chapters"]) == 2

    def test_raises_on_missing_accepted(self, tmp_path: Path) -> None:
        with pytest.raises(PublishError, match="No accepted Book Expression"):
            PublishingSnapshot(tmp_path)

    def test_raises_on_no_book_dir(self, tmp_path: Path) -> None:
        (tmp_path / "book").mkdir(parents=True)
        with pytest.raises(PublishError, match="No accepted Book Expression"):
            PublishingSnapshot(tmp_path)

    def test_snapshot_identity_fallback(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        identity = project / "story_identity.yaml"
        identity.unlink()
        snapshot = PublishingSnapshot(project)
        assert snapshot.title == "The Lantern at Low Water"
        assert snapshot.author == "Unknown Author"

    def test_chapter_parsing(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        snapshot = PublishingSnapshot(project)
        chapters = snapshot._parsed["chapters"]
        assert len(chapters) == 2
        assert chapters[0]["id"] == "chapter_01"
        assert chapters[1]["id"] == "chapter_02"
        assert all(ch["body"] for ch in chapters)


