class TestPublishFunction:
    def test_publish_html(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        out = tmp_path / "pub.html"
        result = publish(project, formats=["html"], html_output=out)
        assert result["snapshot_id"].startswith("pub_")
        assert result["title"] == "The Lantern at Low Water"
        assert out.exists()

    def test_publish_epub(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        out = tmp_path / "pub.epub"
        result = publish(project, formats=["epub"], epub_output=out)
        assert result["snapshot_id"].startswith("pub_")
        assert out.exists()

    def test_publish_both_formats(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        out_dir = tmp_path / "output"
        out_dir.mkdir()
        result = publish(project, formats=["html", "epub"], output_dir=out_dir)
        assert len(result["renderers"]) == 2
        assert (out_dir / "book.html").exists()
        assert (out_dir / "book.epub").exists()

    def test_publish_saves_snapshot(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        publish(project, formats=["html"], html_output=tmp_path / "book.html")
        assert (project / ".auteur" / "publishing" / "latest.yaml").exists()

    def test_publish_error_on_bad_format(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        with pytest.raises(PublishError, match="unknown format"):
            publish(project, formats=["pdf"])


