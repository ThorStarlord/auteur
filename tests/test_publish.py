class TestEpubRendering:
    def test_render_epub(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        snapshot = PublishingSnapshot(project)
        output = tmp_path / "book.epub"
        result = snapshot.render_epub(output)
        assert result["format"] == "epub"
        assert result["output_hash"].startswith("sha256:")
        assert output.exists()
        assert output.stat().st_size > 200

    def test_epub_is_valid_zip(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        snapshot = PublishingSnapshot(project)
        output = tmp_path / "valid.epub"
        snapshot.render_epub(output)
        with zipfile.ZipFile(output, "r") as zf:
            names = zf.namelist()
        assert "mimetype" in names
        assert "META-INF/container.xml" in names
        assert "OEBPS/content.opf" in names
        assert "OEBPS/nav.xhtml" in names
        assert "OEBPS/stylesheet.css" in names
        assert any(n.startswith("OEBPS/chapter_") and n.endswith(".xhtml") for n in names)

    def test_epub_chapter_count(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        snapshot = PublishingSnapshot(project)
        output = tmp_path / "count.epub"
        snapshot.render_epub(output)
        with zipfile.ZipFile(output, "r") as zf:
            names = zf.namelist()
        chapters = [n for n in names if n.startswith("OEBPS/chapter_") and n.endswith(".xhtml")]
        assert len(chapters) == 2

    def test_epub_content_has_title(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        snapshot = PublishingSnapshot(project)
        output = tmp_path / "title.epub"
        snapshot.render_epub(output)
        with zipfile.ZipFile(output, "r") as zf:
            opf = zf.read("OEBPS/content.opf").decode("utf-8")
        assert "The Lantern at Low Water" in opf
        assert "Unknown Author" in opf

    def test_epub_no_markers(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        snapshot = PublishingSnapshot(project)
        output = tmp_path / "clean.epub"
        snapshot.render_epub(output)
        with zipfile.ZipFile(output, "r") as zf:
            for name in zf.namelist():
                if name.endswith(".xhtml"):
                    data = zf.read(name).decode("utf-8")
                    assert "<!-- auteur:" not in data

    def test_epub_raises_on_existing(self, tmp_path: Path) -> None:
        project = _make_book(tmp_path)
        snapshot = PublishingSnapshot(project)
        output = tmp_path / "exists.epub"
        output.write_bytes(b"existing")
        with pytest.raises(FileExistsError):
            snapshot.render_epub(output)


